def generate_scaffold(
    channel_name: str,
    plugin_dir: str | Path | None = None,
    *,
    overwrite: bool = False,
) -> Path:
    """Write a starter adapter into ``plugin_dir`` for ``channel_name``.

    Returns the path of the newly-created file. Raises ``ValueError`` for
    invalid names; raises ``FileExistsError`` when the target already
    exists and ``overwrite=False``.
    """
    if not _VALID_NAME_RE.match(channel_name):
        raise ValueError(
            f"channel_name {channel_name!r} must match {_VALID_NAME_RE.pattern} "
            "(lowercase, snake_case, starts with a letter)"
        )

    if plugin_dir is None:
        plugin_dir = Path.home() / ".predacore" / "channels"
    plugin_dir = Path(plugin_dir).expanduser()
    plugin_dir.mkdir(parents=True, exist_ok=True)

    target = plugin_dir / f"{channel_name}.py"
    if target.exists() and not overwrite:
        raise FileExistsError(
            f"{target} already exists — pass overwrite=True to replace it"
        )

    body = _SCAFFOLD_TEMPLATE.format(
        channel_name=channel_name,
        channel_human=channel_name.replace("_", " ").title(),
        class_name=_class_name(channel_name),
        env_var=_env_var_name(channel_name),
    )
    target.write_text(body)
    logger.info("Wrote channel scaffold: %s", target)
    return target
