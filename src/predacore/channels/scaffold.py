"""Channel-adapter scaffold generator (T10a).

``predacore channel scaffold <name>`` writes a fully-typed starter
adapter into ``~/.predacore/channels/<name>.py`` so users can add
new channels without copy-pasting from existing ones.

The scaffold:
  * inherits from :class:`predacore.gateway.ChannelAdapter`
  * implements ``start`` / ``stop`` / ``send`` with TODO comments
  * shows the token-from-env pattern (used by every shipped adapter)
  * includes a streaming-buffer factory hook (T9 pattern, copy-paste ready)
  * adds a docstring listing the next steps (config edit, channel_install,
    secret_set)

Generation is plain string templating — no Jinja, no extra deps. The
output runs as-is the moment the user fills in the three TODO blocks
(API connect, on_message wiring, send call).
"""
from __future__ import annotations

import logging
import re
from pathlib import Path

logger = logging.getLogger(__name__)

# ``channel_name`` is matched against ``config.channels.enabled``, so we
# enforce the same allowed-character set the registry uses (lower snake).
_VALID_NAME_RE = re.compile(r"^[a-z][a-z0-9_]*$")


def _class_name(channel_name: str) -> str:
    """``matrix`` -> ``MatrixAdapter``; ``ms_teams`` -> ``MsTeamsAdapter``."""
    parts = channel_name.split("_")
    return "".join(p.capitalize() for p in parts) + "Adapter"


def _env_var_name(channel_name: str) -> str:
    """``matrix`` -> ``MATRIX_TOKEN`` (followed by the existing _SECRET_MAP convention)."""
    return f"{channel_name.upper()}_TOKEN"


